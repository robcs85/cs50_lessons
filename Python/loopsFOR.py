# Criando uma lista simples para imprimir numeros de 1 a 6 utilizando laço de repetição for

# Neste laço for a cada interação pelo laço for, imprima o que estiver na variavel (i)
for i in [0, 2, 3, 4, 5]:
    print(i, end=" - ")

# Posso conseguir este mesmo resultando sem digitar os numeros, apenas utilizado a funça
# range(6) passando a mesma quantidade de numero que eu queo que seja impressa
# end= separa o codigo com hifen ou barra na frente
for ranges in range(6):
    print(ranges, end=" |- ")