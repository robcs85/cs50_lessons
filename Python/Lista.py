# Criando uma lista de caracteres String
nomes = ["Joey", "Rachel", "Monica", "Ross"]

# a função APPEND irá adicionar ao final da lista, neste caso o nomes passados dentro da função
nomes.append("Chandler, Phoebe")

# A função SORT() literalmente seleciona por ordem alfabetica os nomes na lista
nomes.sort()

# ::-1 Inverte a saida feita pelo SORT(). ao invés de sair conforme a função faz, por ordem alfabetica, inverte
print(nomes)